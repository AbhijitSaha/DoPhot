	SUBROUTINE ELLIPSE(B5,A6,B7,AREA,AMAJ,AMINO,ANGLE)
	A5 = 1/B5
	A7 = 1/B7
	ANGLE = ATAN2(-2*A6, A7 - A5)/2
	ROOT = SQRT((A5 - A7)**2 + 4*A6**2)	
	ROOT1 = (A5 + A7 + ROOT)/2
	ROOT2 = ROOT1 - ROOT			
	IF (ROOT2 .GE. 0) THEN
	  AREA = 6.2832/SQRT(ROOT1*ROOT2)
          AMAJ = 2.35482*SQRT(1./ROOT2)
	ELSE
	  AREA = 0
          AMAJ = 0
	END IF
	AMINO = 2.35482*SQRT(1./ROOT1)
	RETURN
	END
